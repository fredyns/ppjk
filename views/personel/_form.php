<?php

use yii\bootstrap\ActiveForm;
use yii\helpers\Html;
use yii\helpers\StringHelper;
use cornernote\returnurl\ReturnUrl;
use dmstr\bootstrap\Tabs;

/**
 * @var yii\web\View $this
 * @var app\models\Personel $model
 * @var yii\widgets\ActiveForm $form
 */
?>

<div class="personel-form">

    <?php
    $form = ActiveForm::begin([
            'id' => 'Personel',
            'layout' => 'horizontal',
            'enableClientValidation' => true,
            'errorSummaryCssClass' => 'error-summary alert alert-error'
    ]);

    echo Html::hiddenInput('ru', ReturnUrl::getRequestToken());
    ?>

    <div class="">
        <?php $this->beginBlock('main'); ?>

        <p>

            <!-- attribute companyProfile_id -->
            <?=
            // generated by schmunk42\giiant\generators\crud\providers\core\RelationProvider::activeField
                $form
                ->field($model, 'companyProfile_id')
                ->dropDownList(
                    \yii\helpers\ArrayHelper::map(app\models\CompanyProfile::find()->all(), 'id', 'name'),
                    [
                    'prompt' => 'Select',
                    'disabled' => (isset($relAttributes) && isset($relAttributes['companyProfile_id'])),
                    ]
            );
            ?>

            <!-- attribute profile_id -->
            <?=
            // generated by schmunk42\giiant\generators\crud\providers\core\RelationProvider::activeField
                $form
                ->field($model, 'profile_id')
                ->dropDownList(
                    \yii\helpers\ArrayHelper::map(app\models\Profile::find()->all(), 'id', 'name'),
                    [
                    'prompt' => 'Select',
                    'disabled' => (isset($relAttributes) && isset($relAttributes['profile_id'])),
                    ]
            );
            ?>

            <!-- attribute title -->
            <?=
            $form->field($model, 'title')->textInput(['maxlength' => true])
            ?>
        </p>

        <?php $this->endBlock(); ?>

        <?=
        Tabs::widget([
            'encodeLabels' => false,
            'items' => [
                [
                    'label' => Yii::t('app', 'Personel'),
                    'content' => $this->blocks['main'],
                    'active' => true,
                ],
            ],
        ]);
        ?>

        <hr/>

        <?php echo $form->errorSummary($model); ?>

        <?=
        Html::submitButton(
            '<span class="glyphicon glyphicon-check"></span> '.
            ($model->isNewRecord ? 'Create' : 'Save'),
            [
            'id' => 'save-'.$model->formName(),
            'class' => 'btn btn-success'
            ]
        );
        ?>

    </div>

    <?php ActiveForm::end(); ?>

</div>

